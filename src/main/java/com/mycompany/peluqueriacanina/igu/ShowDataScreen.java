    private void loadTable() {
        DefaultTableModel logicTable = new DefaultTableModel() {
            @Override
            public boolean isCellEditable (int row, int column){
                return false;
            }
        };
        
        String titles[] = {"ID", "NAME", "BREED", "COLOR", "ALLERGIC", "SPECIAL ATENTTION", "OWNER NAME", "PHONE"};
        logicTable.setColumnIdentifiers(titles);
        
        List<Pet> petList = control.readAllPet();
        if (petList != null){
            for (Pet p: petList){
                
                Object[] object = {p.getNum_customer(), p.getName_dog(), p.getBreed(), p.getColor(), p.getAllergic(), p.getSpecial_atenttion(), p.getOwner().getName(), p.getOwner().getPhone()};
                
                logicTable.addRow(object);
            }
        }
        
        dataTable.setModel(logicTable);
    }
    
    public void showMessage(String title, String message, String type){
        JOptionPane optionPane = new JOptionPane(message);
        if (type.equalsIgnoreCase("information")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (type.equalsIgnoreCase("error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        JDialog dialog = optionPane.createDialog(title);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true); 
    }
