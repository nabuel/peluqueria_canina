    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        if(dataTable.getRowCount() > 0){
            if (dataTable.getSelectedRow() != -1){
                int id = Integer.parseInt(String.valueOf(dataTable.getValueAt(dataTable.getSelectedRow(), 0)));
                
                control.deleteOwner(control.readPet(id).getOwner().getId_owner());
                control.deletePet(id);
                
                showMessage("Deleted Correctly", "Delete Complete", "information");
                loadTable();
            }else {
            
                showMessage("Element not selected", "Select a row for deleting.","error");
        
            }
        }else {
            showMessage("Deleted Unsuccessfully", "There are no elements to delete","information");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

